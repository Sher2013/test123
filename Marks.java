package manyOne;

public class Marks {
	private String subject;
	private Integer mark;
	
	public Marks(String subject, Integer mark) 
	{
		
		this.subject = subject;
		this.mark = mark;
		
	}

	public String getSubject() {
		return subject;
	}

	public Integer getMark() {
		return mark;
	}
	
	

}
